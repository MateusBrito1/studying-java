package ProgramacaoEstruturada;

public class Aula13Arays {
    public static void main(String[] args) {

        String[] nomes = new String[3];

        nomes[0] = "Itachi";
        nomes[1] = "Kurosaki";
        nomes[2] = "Luffy";

        for (String nome : nomes) {
            System.out.println(nome);
        }

    }
}
